// tapdance stuff
void dance_layers(qk_tap_dance_state_t *state, void *user_data) {
  switch (state->count) {
    case 1:
      layer_move(_ONEHANDNAV);
      break;
    case 2:
      layer_move(_ONEHANDPAD);
      break;
    case 3:
      layer_move(_ONEHANDF);
  }
}

